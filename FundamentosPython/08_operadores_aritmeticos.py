# Este es el orden de ejecución de operaciones aritméticas (Izquierda -> Derecha)
""" 
P - paréntesis
E - exponentes
M - multiplicación
D - división
A - adición
S - sustracción
 """

print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)
print(10 // 3)
print(10 % 2)
print(10 % 3)
print(2**3)

print(2**3 + 3 - 7 / 1 // 4)


