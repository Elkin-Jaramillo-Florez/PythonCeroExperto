# ======================================
# TIPOS DE DATOS: ENTEROS, FLOTANTES Y BOOLEANOS
# ======================================

# Números enteros
x = 10
print(f"x = {x} (tipo: {type(x)})")  # int

# Números flotantes
y = 5.678
print(f"y = {y} (tipo: {type(y)})")  # float

# Notación científica (comentado, pero útil conocer)
# z = 1.3e5     # 130000.0
# a = 1.3e-5    # 0.000013
# print(f"z = {z}")
# print(f"a = {a}")

# Operaciones aritméticas
print(f"Suma x + y = {x + y}")
print(f"Suma y + y = {y + y}")

# Booleanos
is_true = True
is_false = False
print(f"is_true = {is_true} (tipo: {type(is_true)})")  # bool
print(f"is_false = {is_false} (tipo: {type(is_false)})")  # bool
